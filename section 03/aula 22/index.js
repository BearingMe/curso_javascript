/**
 * Operadores lógicos 
 * && e -> Todas as operações precisam ser true para retornar true
 * || ou -> Uma das operações precisam ser true para retornar true
 * ! negação
*/

`eu tenho dinheiro? estou com fome?`
console.log(true && true); // true

`eu tenho dinheiro? está sol lá fora?`
console.log(true || false); // true